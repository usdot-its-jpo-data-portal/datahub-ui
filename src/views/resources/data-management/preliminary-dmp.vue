    <h1>Preliminary DMP Instructions</h1>
    <h2 id="prelim-introduction">Introduction</h2>
    <p>
      This following web page provides guidelines for creating a preliminary Data Management Plan (DMP) to make it easier for projects to prepare their preliminary DMP using a consistent format. To see a downloadable version of these instructions, as well as an associated template, please reference the <router-link id="data-management-template-id-in-page" to="/resources/data-management/template-and-instructions" class="in-page-link">Templates &amp; Instructions</router-link> tab. To provide content and updates to these instructions or the associated template, you may provide comments through our <a target=”_blank” href="https://github.com/usdot-its-jpo-data-portal/data-access-coe-resources">GitHub page<img class="in-line-external-link-icon" src="/images/icons/external-link_1b1b1b.svg" alt="External from DOT link icon" title="External link. Opens in a new tab."></a>.
    </p>
    <p>
      These guidelines are based off of U.S. DOT’s National Transportation Library’s <a target=”_blank” href="https://ntl.bts.gov/public-access/creating-data-management-plans-extramural-research">Guidance for Creating Data Management Plans for Extramural Research<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab."></a>, along with additional clarification on U.S. DOT’s expectations for DMPs. These instructions inform applicants of elements the U.S. DOT would like to see in the preliminary DMP but do not serve as an approved government form or template. Applicants should use their best judgement in determining what information to include and whether or not additional information should be incorporated into the preliminary DMP. 
    </p>
    <p>
      A preliminary DMP is a document submitted as part of an application that describes how applicants plan to handle digital datasets over the course of a research project pursuant to federal and Departmental requirements. A preliminary DMP must be provided as part of an application for any Intelligent Transportation Systems (ITS) Joint Program Office (JPO)-funded project or project adhering to the Data Access Center of Excellence best practices, and should adequately identify: 1) the data to be collected, 2) how the data will further the goals of the U.S. DOT, 3) how the data will be made accessible, and 4) how the data will be stored.
    </p>
    <p>The United States Department of Transportation (U.S. DOT) understands elements in the preliminary DMP may be updated as the research project develops. However, the U.S. DOT seeks a clear explanation of the research project’s data and the data’s alignment with the U.S. DOT’s research goals. After funds are awarded, this preliminary DMP shall be updated regularly to capture pertinent changes to a project's data management procedures and/or data.</p>
    
    <h2 id="prelim-project-overview">1. Project Overview</h2>
    <p>Please provide summary information about the research project and its goals, as well as how the project’s data helps the U.S. DOT achieve its goals.</p>
      <p>Suggested elements for this section include, but are not limited to:
      <ul>
        <li>
          <strong>Project Title:</strong> Please provide the title of the proposed project.
        </li>
        <li>
          <strong>Project Goals and Objectives:</strong> Briefly describe the goals and objectives of this project, as well as how the data collected in this project will help the U.S. DOT achieve its goals and objectives.
        </li>
        <li>
          <strong>Project Description:</strong> Briefly describe the research project, including project design and execution.
        </li>
      </ul>
    </p>

    <h2 id="prelim-data-overview">2.	Data Overview</h2>
    <p>Please provide a description of the nature, scope, and scale of the data that will be collected and/or produced. To the extent possible, please describe the datasets that are anticipated to be collected throughout the research project and any details for these datasets.</p>
    <p>Suggested elements for this section include, but are not limited to:
    <ul>
      <li>
        <strong>Data Title:</strong> Provide the anticipated data and/or dataset title(s) for the research project.
      </li>
      <li>
        <strong>Description:</strong> Briefly describe the purpose of the data and/or datasets that are anticipated to be collected during the research project.
      </li>
      <li>
        <strong>Type/Scale:</strong> Specify the type and scale of the data that will be generated (e.g., numerical data, image data, text sequences, video, audio, database, modeling data, source code, etc.) for the research project’s data and/or datasets.
      </li>
      <li>
        <strong>Collection Method:</strong> Describe the methods for collecting and/or acquiring the data (e.g., simulated; observed; experimental; software; physical collections; sensors; satellite; enforcement activities; researcher-generated databases, tables, and/or spreadsheets; instrument generated digital data output such as images and video; etc.), and specify by dataset if appropriate.
      </li>
      <li>
        <strong>Data File Format(s):</strong> Provide the data file format(s) the data and/or datasets are anticipated to be in when they are made accessible to the U.S. DOT (e.g. .csv, .txt, json, newline json, etc.). If not yet known, explain to the best of your ability.
      </li>
      <li>
        <strong>Metadata:</strong> Indicate what metadata schema you will use to describe the data and/or dataset in various contexts. If not yet known, explain to the best of your ability. U.S. DOT expects metadata in several contexts, including:
        <ul>
          <li>At the field level, describing the elements in the data and/or dataset</li>
          <li>At the asset level, describing the contents of the data or dataset generally, in accordance with the <a target=”_blank” href="https://project-open-data.cio.gov/v1.1/schema/">Project Open Data Metadata Schema<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab."></a> or another appropriate standard</li>
          <li>At any other level that may be appropriate for the data and/or dataset</li>
        </ul>
      </li>
    </ul>
      
    <h2 id="prelim-data-stewardship">3. Data Stewardship</h2>
    <p>Please provide details around data stewardship. Data stewardship involves proper management of data throughout the data lifecycle. This includes, but is not limited to, maintaining data quality and safeguarding the data.</p>
    <h3>3.1 Data Owner and Steward</h3>
    <p>
      Given the partnership-driven nature of the U.S. DOT, many determinations around project data, including its ownership, will be made during negotiations between project partners. Applicants should indicate in their preliminary DMP the status of negotiations at the time of application. 
    </p>
    <p>
      Please define the data owner and data steward for the anticipated data and/or datasets that will be produced as part of the research project. It is acceptable for multiple datasets to have the same data owner and/or data steward.
    </p>
    <p> 
      Suggested elements for this section include, but are not limited to:
      <ul>
        <li>
          <strong>Dataset Title:</strong> Please provide the data and/or datasets title(s) that will be assigned the designated data owner and/or data steward. You may break out data and/or datasets that have separate data owners and/or data stewards.
        </li>
        <li>
          <strong>Data Owner:</strong> List who the data owner of the data or dataset is. The data owner is the person or organization that has the authority, ability, and responsibility to access, create, modify, store, use, share, and protect the data. Data owners have the right to delegate these privileges and responsibilities to other parties. Please note the data owner may be the U.S. DOT for U.S. DOT-funded research projects.
        </li>
        <li>
          <strong>Data Steward:</strong> List who the data steward for the data or dataset is. The data steward is, at the direction of the data owner, the person or organization that is delegated the privileges and responsibilities to manage, control, and maintain the quality of a data asset throughout the data lifecycle. The data steward may also apply appropriate protections, restrictions, and other safeguards depending on the nature of the data, subject to the direction of the data owner. The data steward may be the recipient of U.S.DOT-funded research projects or one of the recipient’s partner organizations.
        </li>
      </ul>
    </p>
    <h3>3.2 Access Level</h3>
    <p>
      Providing appropriate data access to safeguard data is a key aspect of data stewardship. In accordance with the OPEN Government Data Act, datasets should be made publicly accessible unless specific concerns require the data to have controlled-access. Please detail which data may require controlled-access, reasons for controlling access to data, and plans for safeguarding these data.
    </p>
    <p>
      Controlled-access is defined as restricting access to certain groups of persons due to data containing personally identifiable information (PII), information that threatens privacy of an individual or group, information that threatens confidentiality of a person or group, and/or contains confidential business information (CBI). 
    </p>
    <h4>3.2.1 Can all of the data from this project be shared with the public, or is controlled-access required for at least some of the data?</h4>
    <p>
      Please check the box for the access level that best applies to the project’s data:
      <ul>
        <li>
          <strong>All Public Access:</strong> There is no anticipated data requiring controlled-access.
        </li>
        <li>
          <strong>Some/All Controlled-Access</strong> There is anticipated data in one or more datasets requiring controlled-access (e.g. Personally Identifiable Information [PII] or Confidential Business Information [CBI]).
        </li>
      </ul>
    </p>
    
    <h4>3.2.2	Datasets Requiring Controlled-Access</h4>
    <p>
      The information below is only required if the “Some/All Controlled-Access” box is selected in 3.2.1. Suggested elements for this section include, but are not limited to:
      <ul>
        <li>
          <strong>Dataset Title:</strong> Provide the anticipated data/dataset(s) that will be at the specified access level.
        </li>
        <li>
          <strong>Reasons for Controlled-Access:</strong> For any data requiring controlled-access, please detail what in the data is anticipated to require controlled-access and why controlled-access may be required for those elements. Please provide details on all elements of the data and/or datasets that are anticipated to require controlled-access.
        </li>
        <li>
          <strong>Safeguarding Methods and Processes:</strong> Describe the planned methods and processes that will be taken to safeguard the data requiring controlled-access, such as redacting data before providing public access to the dataset. Please elaborate on the specific methods, for example blurring license plates in videos, geofencing sensor data, removing persistent IDs, etc. Any privacy, ethical or confidentiality concerns raised due to data sharing should be described.
        </li>
      </ul>
    </p>

    <h4>3.2.3	Informed Consent</h4>
    <p>
      If your project contains human subject research, please describe how informed consent forms will permit sharing with the research community and whether additional steps, such as an Institutional Review Board (IRB) may be used to protect privacy and confidentiality. Please refer to <a target="_blank" href="https://ntl.bts.gov/public-access/creating-data-management-plans-extramural-research"> NTL's web page on Creating Data Management Plans for Extramural Research<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab."></a> for more information.
    </p> 
    
    <h3>3.3	Re-Use, Redistribution, and Derivative Products Policies</h3>
    <p>
      The U.S. DOT is interested in who holds the intellectual property rights for the research project data, whether these rights will transfer to a data archive, whether any copyrights apply to the data, and other aspects that apply to the rights to use the data. Please provide details on these elements in this section.
    </p>
    <p>
      In accordance with the <a target=”_blank” href="https://www.congress.gov/bill/115th-congress/house-bill/1770/text">OPEN Government Data Act<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab."></a>, to the extent practicable data must be made available through an open license that is available at no cost to the public and with no restrictions on copying, publishing, distributing, transmitting, citing or adapting such asset. In this section, please provide the applicable licenses for each dataset and specify if an open license will be used. If the project is U.S. DOT-funded, an open license should be used for the dataset. If a non-open license is anticipated to be used for the dataset, please include a justification for why a non-open license will be used.
    </p>
    <p>
      Suggested elements for this section include, but are not limited to:
      <ul>
        <li>
          <strong>Dataset Title:</strong> Please provide the title(s) of the dataset(s) that are anticipated to use this license.
        </li>
        <li>
          <strong>License Used:</strong> List the license that is anticipated to be used for these data and/or datasets (e.g. Creative Commons – BY version X.0) and provide a URL, or several, with more information on the license. Specify whether the license is an open license or a non-open license. An “’open license’ means a legal guarantee that a data asset is made available (A) at no cost to the public and (B) with no restrictions on copying publishing, distributing, transmitting, citing or adapting such asset. ”, definition referenced from <a target="_blank" href="https://www.congress.gov/bill/115th-congress/house-bill/1770/text ">congress.gov's webpage for H.R.1770 <img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab."></a>, see SEC. 4. OPEN GOVERNMENT DATA. A non-open license does not adhere to these open license criteria. 
        </li>
        <li>
          <strong>Reason(s) for a Non-Open License:</strong> If a non-open license is anticipated to be used for this dataset, provide the reason for using a non-open license. 
        </li>
      </ul>
    </p>

    <h3>3.4	Data Storage, Archiving and Preservation</h3>
    <p>
      Storing and retaining the data is a key part of the data steward’s responsibilities to manage, control, and maintain the quality of a data asset throughout the data lifecycle. 
    </p>
    <p>
      In this section, list all of the data storage systems that will be used to store the research project’s data, provide details on those data systems, and specify how long the data will be stored in each system. Please note that U.S. DOT-funded projects should provide access to the U.S. DOT for a minimum of five years after the award period of performance expires. The U.S. DOT understands that dates provided in this section are anticipated dates and will be updated after award.
    </p>
    <p>
      A public access system provides full data access to the public. A controlled-access system restricts access to certain groups of persons due to data containing personally identifiable information (PII), information that threatens privacy of an individual or group, information that threatens confidentiality of a person or group, and/or contains confidential business information (CBI), please see Requirements Section B. Access Requirements on the <router-link id="data-management-template-id-in-page" to="/resources" class="in-page-link">Guidelines page</router-link>.  An applicant system is managed by the applicant, a U.S. DOT system is managed by the U.S. DOT, and a third-party system is a system that is managed by a person, group or organization other than the U.S. DOT or the applicant.
    </p>
    <p>
      Suggested elements for this section include, but are not limited to:
      <ul>
        <li>
          <strong>Data Storage System Name:</strong> Provide the name of the anticipated data storage system(s) the dataset(s) will be stored in. Provide a URL/URLs or other means of accessing the data storage system.
        </li>
        <li>
          <strong>Data Storage System(s) Type:</strong> For each data storage system, state whether the data storage system is:
          <ul>
            <li>“Applicant &mdash; Public System”</li>
            <li>o	“Applicant &mdash; Controlled System”</li>
              <ul>
                <li>If you are using an applicant-managed system, please state whether the system is currently operational, under development, or will be developed as part of this project.</li>
              </ul>
            <li>“U.S. DOT-managed &mdash; Public System”</li>
            <li>“U.S. DOT-managed &mdash; Controlled-Access System”</li>
            <li>“Third-party &mdash; Public System”</li>
            <li>“Third-party &mdash; Controlled-Access System”</li>
          </ul>  
        </li>
        <li>
          <strong>Dataset Title(s):</strong> Provide all data and/or datasets that are anticipated to be stored in each data storage system.
        </li>
        <li>
          <strong>Initial Storage Date:</strong> Provide the anticipated initial date that data will be available in each data storage system. If you are live-streaming data, this may be the initial ingest date. You can write this date as a period after award (e.g. three months after award, six months after award, etc.)
        </li>
        <li>
          <strong>Frequency of Update:</strong> Please provide how frequently the data will be updated in the data storage system once ingestion begins. Please note the U.S. DOT prefers obtaining the data as quickly as possible after collection, preferably in near real-time and/or through automated uploads. This is in accordance with the OPEN Government Data Act’s requirement to maximize accessibility to federally-funded datasets.
        </li>
        <li>
          <strong>Archiving and Preservation Period:</strong> Provide the duration for which the data and/or dataset will be maintained in each data storage system.
        </li>
      </ul>
    </p>

