    <h2 id="Scope">Scope</h2>
    <p>
      These requirements apply to all research projects funded, either fully or partially, by the ITS JPO.
      Any ITS JPO-funded research project, including new awards and modifications and extensions to existing
      awards, is subject to its requirements. These requirements may be waived by the ITS JPO for research
      projects that do not involve the collection or creation of any data.
    </p>

    <h2 id="Definitions">Definitions</h2>
    <ul>
      <li>
        <strong>Applicants</strong>: Persons responding to a solicitation by the U.S. DOT for an ITS JPO-funded grant, contract, or other funding agreement.
      </li>
      <li>
        <strong>Award</strong>: Any funding agreement, such as a contract or grant, signed by a contracting officer that permits the expenditure of government funds or use of such government resources to a provider of the goods or services requested in the funding agreement.
      </li>
      <li>
        <strong>Awardee</strong>: An organizational entity or individual that receives a written agreement from a U.S. DOT contracting officer to receive funds from the ITS JPO and/or the U.S. DOT in order to carry out a project or activity.
      </li>
      <li>
        <strong>Confidentiality</strong>: The ability of only authorized users to access controlled access data.
      </li>
      <li>
        <strong>Confidential business information</strong>: Trade secrets and commercial or financial information obtained from a person and privileged or confidential.
      </li>
      <li>
        <strong>Controlled access</strong>: Restricting access to certain groups of persons due to data containing personally identifiable information (PII), information that threatens privacy of an individual or group, information that threatens confidentiality of a person or group, and/or contains confidential business information.
      </li>
      <li>
        <strong>Data</strong>: Recorded information, regardless of form or the media on which the data is recorded.
      </li>
      <li>
        <strong>Data discovery</strong>: The ability to find data from search engine, such as the ITS DataHub.
      </li>
      <li>
        <strong>Data integrity</strong>: The assurance that digital information is uncorrupted and can only be accessed or modified by those authorized to do so. Integrity involves maintaining the consistency, accuracy and trustworthiness of data over its entire lifecycle.
      </li>
      <li>
        <strong>Data owner</strong>: The person or organization that has the authority, ability, and responsibility to access, create, modify, store, use, share, and protect data. Data owners have the right to delegate these privileges and responsibilities to other parties. Please note, for U.S. DOT-funded projects this may be U.S. DOT.
      </li>
      <li>
        <strong>Data provider</strong>: An individual or team that collects, prepares and/or submits research datasets to the ITS JPO.
      </li>
      <li>
        <strong>Data security</strong>: Protective digital privacy measures that are applied to prevent unauthorized access to computers, databases and websites. Data security also protects data from corruption.
      </li>
      <li>
        <strong>Data steward</strong>: At the direction of the data owner, the person or organization that is delegated the privileges and responsibilities to manage, control, and maintain the quality of a data asset throughout the data life cycle. The data steward may also apply appropriate protections, restrictions, and other safeguards depending on the nature of the data, subject to the direction of the data owner.
      </li>
      <li>
        <strong>Derived data</strong>: Data derived thorough analysis of other data and shared as part of a final report to communicate the methodology and findings of a research project.
      </li>
      <li>
        <strong>Limited rights data</strong>: Data, other than computer software, that embody trade secrets or are commercial or financial and confidential or privileged, to the extent that such data pertain to items, components, or processes developed at private expense, including minor modifications.
      </li>
      <li>
        <strong>Metadata</strong>: Structural or descriptive information about data such as content, format, source, rights, accuracy, provenance, frequency, periodicity, granularity, publisher or responsible party, contact information, method of collection, and other descriptions.
      </li>
      <li>
        <strong>Personally Identifiable Information (PII)</strong>: Information that alone or in conjunction with additional information can threaten the privacy or identity of an individual.
      </li>
      <li>
        <strong>Primary data</strong>: Data collected during research that has not had significant analysis performed on it.
      </li>
      <li>
        <strong>Privacy</strong>: The ability of an individual to keep his or her personal information to themselves.
      </li>
      <li>
        <strong>Public</strong>: The general public, as well as qualified researchers or other similar entities.
      </li>
      <li>
        <strong>Public access</strong>: Data asset is or could be made publicly available to all without restrictions.
      </li>
      <li>
        <strong>Research</strong>: Creative work undertaken on a systemic basis in order to increase the stock of knowledge, including knowledge of man, culture and society.
      </li>
      <li>
        <strong>Research project</strong>: A project designed to accomplish a research objective.
      </li>

    </ul>

    <h2 id="Requirements">Requirements</h2>
    <ol>
      <li class="dh-listtype__upperalpha">
        <h3>Data Management Plan</h3>
      </li>
      <p>
        Applicants must include a draft Data Management Plan (DMP) in their proposal. The U.S. DOT will use the
        information in the draft DMP as part of its evaluation criteria for award and may ask for clarifications or
        changes from applicants prior to award to ensure the U.S. DOT and/or public receives appropriate data access.
        The draft DMP must provide a preliminary overview of data that may be collected or created through the project,
        and include the following:
      </p>
      <ol>
        <li>
          DATA DESCRIPTION: Address the nature, scope, and scale of the data that will be collected or created.
          Define data elements within a detailed data dictionary.
        </li>
        <li>
          ACCESS POLICIES: Describe any access restrictions that may apply to your data.
        </li>
        <li>
          DATA STORAGE AND RETENTION: Describe where you intend to store your data and why you have chosen that
          particular option. Different systems may be designated for different types of data (e.g., public, non-public).
          Also describe how long you intend to retain your data in these systems.
        </li>
        <li>
          DATA RIGHTS: Describe who will hold the intellectual property rights for your data, and any other legal
          requirements that might need to be addressed.
        </li>
      </ol>
      <p>
        Awardees must submit an updated DMP within 60 days after effective date of award and receive ITS JPO approval
        for the approach described. The updated DMP must include the following:
      </p>
      <ol>
        <li>
          DATA DESCRIPTION: Address the nature, scope, and scale of the data that will be collected. Define data elements
          within a detailed data dictionary.
        </li>
        <li>
          ACCESS POLICIES: Describe any access restrictions that may apply to your data.
        </li>
        <li>
          DATA STORAGE AND RETENTION: Describe where you intend to store your data and why you have chosen that particular
          option. Different systems may be designated for different types of data (e.g., public, non-public). Also describe
          how long you intend to retain your data in these systems.
        </li>
        <li>
          DATA RIGHTS: Describe who will hold the intellectual property rights for your data, and any other legal
          requirements that might need to be addressed.
        </li>
        <li>
          TIMING: Describe when your data will be shared, and how frequently it will be updated.
        </li>
      </ol>
      <p>
        Awardees must then maintain the DMP throughout the period of performance with more detailed, updated information
        as it becomes available. At a minimum, the Awardee must update the DMP every six months and any time the Awardee
        shares a new dataset or changes data standards used. By the end of the project, the DMP must be a complete and
        accurate record of all data collected or created through the project.
        <br>
        The DMP must demonstrate the project's full compliance with these requirements and all applicable
        U.S. DOT and federal policies and guidance.
      </p>

      <li class="dh-listtype__upperalpha">
        <h3>Access Requirements</h3>
      </li>
      <p>
        Consistent with federal policy (
        <a href="https://www.congress.gov/bill/115th-congress/house-bill/4174/text#toc-H8E449FBAEFA34E45A6F1F20EFB13ED95" target="_blank" rel="noopener noreferrer">
          OPEN Government Data Act<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab."></a>
        ),awardees must provide public access to data collected or created through the project unless a specific privacy,
        confidentiality, security, or other valid restriction is identified. This presumption in favor of openness must be
        reflected in the research project's DMP. Where valid restrictions to public access exist, awardees must provide
        controlled access to data that is necessary to achieve core research goals. For example, if the purpose of a project
        is to evaluate effectiveness of a new technology, the awardee must provide U.S. DOT evaluators access to enough data
        to support a scientifically-sound evaluation. Furthermore, awardees must make final research results and,
        where possible, sanitized versions of primary data available to the public.
      </p>
      <p>
        Final research results must satisfy the reporting and compliance requirements as set forth in the
        <a href="https://www.transportation.gov/sites/dot.gov/files/docs/Official DOT Public Access Plan.pdf" target="_blank" rel="noopener noreferrer">
          U.S.DOT Public Access Plan<img class="in-line-dot-link-new-tab" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab.">
        </a>
        , including, but not limited to, the use of Open Researcher and Contributor ID (ORCID) numbers, the creation
        and maintenance of a research project record in the Transportation Research Board’s (TRB) Research in Progress (RiP)
        database, and the timely and complete submission of all required publications and associated digital datasets as such
        terms are defined in the
        <a href="https://www.transportation.gov/sites/dot.gov/files/docs/Official DOT Public Access Plan.pdf" target="_blank" rel="noopener noreferrer">
          U.S. DOT Public Access Plan<img class="in-line-dot-link-new-tab" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab.">
        </a>
        . Additional information about how to comply with the requirements can be found at:
        <a href="https://ntl.bts.gov/public-access/how-comply" target="_blank" rel="noopener noreferrer">
          https://ntl.bts.gov/public-access/how-comply<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab.">
        </a>
        . The ITS JPO will assist awardees in compliance with these broader U.S. DOT requirements.
      </p>

      <li class="dh-listtype__upperalpha">
        <h3>Data Storage and Retention</h3>
      </li>
      <p>
        Awardees must store their data in systems that provide an appropriate level of user access, functionality and data
        lifecycle management. For more guidelines on acceptable data storage systems for U.S. DOT, see 
        <a href="https://ntl.bts.gov/public-access/guidelines-evaluating-repositories" target="_blank" rel="noopener noreferrer">
          Guidelines for Evaluating Repositories for Conformance to the DOT Public Access Plan
        <img class="in-line-dot-link-new-tab" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab.">
        </a>. 
      </p>
      <p>
        Awardees may use different systems to store different types of data (e.g., public, non-public, primary, derived,
        streaming, static). The ITS JPO has designated several public and controlled access data systems that awardees can use
        to meet their data access and retention requirements, including registering metadata in the
        <a href="https://its.dot.gov/data/" target="_blank" rel="noopener noreferrer">
          ITS DataHub<img class="in-line-dot-link-new-tab" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab.">
        </a>.
        Applicants are encouraged to use these shared resources but can propose alternative systems for storing and providing
        access to data via their draft DMP and supplemental information. If ITS JPO determines that a proposed system does not
        provide an appropriate level of user access and functionality and data lifecycle management, the awardee must use the
        systems designated by the ITS JPO. Wherever possible, these determinations will be made prior to award.
      </p>
      <p>
        Datasets in ITS DataHub are required to be retained for a minimum of five years in accordance
        with the
        <a target="_blank" href="https://urldefense.proofpoint.com/v2/url?u=https-3A__www.fhwa.dot.gov_legsregs_directives_orders_hoit-2Dhq-5F.cfm&d=DwMFAg&c=f4NRRID3zFYDyClb0wZXwA&r=u7RoqcKhr0ZTyUrh9-9vLPNfCjiY1PN51nvbAJRd86I&m=RJHBZQZ681YIadOiU_QhZX08QGI_Sbik_GmJHsmAyOg&s=AvfhqvHzwgtD4YBmX9XGz_Z4GeLyer-H3BMvjy12Amk&e=" rel="noopener noreferrer">ITS DataHub Records Disposition Manual<img class="in-line-external-link-icon" src="/images/icons/external-link_1b1b1b.svg" alt="External from DOT link icon" title="External link. Opens in a new tab."></a>,
        NARA schedule number
        <a target="_blank" href="https://urldefense.proofpoint.com/v2/url?u=https-3A__www.archives.gov_files_records-2Dmgmt_rcs_schedules_departments_department-2Dof-2Dtransportation_rg-2D0406_daa-2D0406-2D2014-2D0003-5Fsf115.pdf&d=DwMFAg&c=f4NRRID3zFYDyClb0wZXwA&r=u7RoqcKhr0ZTyUrh9-9vLPNfCjiY1PN51nvbAJRd86I&m=RJHBZQZ681YIadOiU_QhZX08QGI_Sbik_GmJHsmAyOg&s=7h17qK4mdzMLn3SHkGIl4PwNGSvuFmqi2wVm9ZiHQEU&e=" rel="noopener noreferrer">DAA-0406-2014-0003<img class="in-line-external-link-icon" src="/images/icons/external-link_1b1b1b.svg" alt="External from DOT link icon" title="External link. Opens in a new tab."></a>.
        Dataset types may include
        roadway traffic-related data such as vehicle latitude, longitude, heading and speed; travel times on road segments;
        traffic signal timing plans; rail transit schedules; and weather. This retention period begins when U.S. DOT first
        receives access to a dataset.
      </p>
      
      <li class="dh-listtype__upperalpha">
        <h3>Data Rights</h3>
      </li>
      <p>
        Consistent with federal policy (
        <a href="https://www.whitehouse.gov/sites/whitehouse.gov/files/omb/memoranda/2013/m-13-13.pdf" target="_blank" rel="noopener noreferrer">
          M-13-13<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab.">
        </a>
        ), awardees must apply open licenses to data collected or created through the
        project so there are no restrictions on copying, publishing, distributing, transmitting, adapting, or otherwise using
        the information. The Open Source Initiative approves open source licenses that are "popular and widely used", a list of which can be found at
        <a href="https://opensource.org/licenses/category" target="_blank" rel="noopener noreferrer">
          https://opensource.org/licenses/category<img class="in-line-external-link-icon" src="/images/icons/external-link_1b1b1b.svg" alt="External from DOT link icon" title="External link. Opens in a new tab.">
        </a> 
        . The ITS JPO uses clauses in
        <a href="https://www.acquisition.gov/content/52227-14-rights-data-general" target="_blank" rel="noopener noreferrer">
          FAR 52.227-14<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab.">
        </a>
        to acquire unlimited rights to data for the government,
        though applicants can claim exemptions and propose limited rights data as described in
        <a href="https://www.acquisition.gov/content/52227-14-rights-data-general" target="_blank" rel="noopener noreferrer">
          FAR 52.227-14<img class="in-line-external-link-icon" src="/images/icons/external-link_1b1b1b.svg" alt="External from DOT link icon" title="External link. Opens in a new tab.">
        </a>
        . Applicants must describe their proposed approach to data rights in their draft DMP, including, where applicable,
        a detailed rationale for limiting the government's rights to data.
      </p>

      <li class="dh-listtype__upperalpha">
        <h3>Data Standards</h3>
      </li>
      <p>
        Consistent with federal policy (
        <a href="https://www.whitehouse.gov/sites/whitehouse.gov/files/omb/memoranda/2013/m-13-13.pdf" target="_blank" rel="noopener noreferrer">
          M-13-13<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab.">
        </a>
        ), awardees must use standards to promote data interoperability and openness.
        Where existing standards are insufficient, awardees must describe in their DMP how they will contribute to the
        development of new standards.
      </p>

      <li class="dh-listtype__upperalpha">
        <h3>Timing</h3>
      </li>
      <p>
        Awardees must provide data in near-real-time, via streaming or batch uploads, throughout the period of data
        collection or creation. The ITS JPO encourages awardees to provide sample or test datasets early in a project to
        refine data dissemination processes and formats, and use automated processes to cut down on the time from data
        generation to dissemination.
      </p>

      <li class="dh-listtype__upperalpha">
        <h3>Metadata and Documentation</h3>
      </li>
      <p>
        Awardees must provide metadata on all data described in their DMP. Awardees can provide additional metadata beyond
        the common core fields if desired, and reference more detailed documentation. Consistent with federal policy (
        <a href="https://www.whitehouse.gov/sites/whitehouse.gov/files/omb/memoranda/2013/m-13-13.pdf" target="_blank" rel="noopener noreferrer">
          M-13-13<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab.">
        </a>
        ), ITS JPO will provide this metadata to the public via the
        <a href="https://its.dot.gov/data/" target="_blank" rel="noopener noreferrer">
          ITS DataHub<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab.">
        </a>
        and other data catalogs to facilitate data discovery, unless a specific privacy, confidentiality, security, or other
        valid restriction is identified. Awardees can claim such restrictions when they submit their metadata.
      </p>
      <p>
        The ITS JPO may request more detailed documentation and data dictionaries where necessary for users to
        understand and use the data.
      </p>
    </ol>

    <h2 id="Implementation-Roles-and-Responsibilities">Implementation Roles and Responsibilities</h2>
    <p>
      There are several key roles for implementing these draft data access requirements. The roles are as follows:
    </p>
    <ul>
      <li>
        Awardee: The primary role is the project principal investigator and ITS DataHub data provider.
        Main responsibilities include: a) creating and maintaining a DMP for their respective research project;
        b) identifying, collecting, managing and retaining research data for their respective project as per their DMP;
        c) preparing and coordinating data submissions with ITS JPO; d) answering questions about their research data;
        e) responding to access requests for their controlled data; e) complying with all ITS JPO Data Access Center of
        Excellence (COE) requirements outlined in these requirements outlined herein.
      </li>
      <li>
        U.S. DOT Project Manager: The primary role is oversight for the research project. A U.S. DOT Project Manager is
        assigned to each ITS JPO-funded project. Main responsibilities include: a) coordinating execution of the project
        with the ITS JPO-funded project team; b) receiving, reviewing and approving the project's DMP;
        c) coordinating with the ITS JPO on research project execution and data submission.
      </li>
      <li>
        ITS JPO: The primary role is to administer the ITS JPO Data Access COE requirements and the
        <a href="https://its.dot.gov/data/" target="_blank" rel="noopener noreferrer">
          ITS DataHub<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab.">
        </a>
        , as well as provide technical assistance to ITS JPO-funded project data providers, project awardees and project
        managers. Main responsibilities include: a) defining requirements, data management standards and
        <a href="https://its.dot.gov/data/" target="_blank" rel="noopener noreferrer">
          ITS DataHub<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab.">
        </a>
        guidelines; b) operating and maintaining the 
        <a href="https://its.dot.gov/data/" target="_blank" rel="noopener noreferrer">
          ITS DataHub<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab.">
        </a>
        ; c) providing technical assistance to awardees to create a DMP and successfully implement the project's DMP.
        While the ITS JPO expects awardees to fully implement their DMP, the ITS JPO will provide technical assistance to
        clarify any ambiguity around data management and assist the project in implementing their DMP.
      </li>
      <li>
        U.S. DOT CIO: The primary role is promoting the effective and efficient design and operation of all major information
        resource management processes within U.S. DOT.
      </li>
    </ul>

