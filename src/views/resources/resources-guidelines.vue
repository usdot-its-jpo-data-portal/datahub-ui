        </a>
      </li>
    </ul>
    </p>
    <p>
      The goal of these guidelines is to:
      <ul>
        <li>Decrease time from research to insight for ITS technologies</li>
        <li>Harmonize data ingest, formats, and analyses across similar ITS data collections</li>
        <li>Facilitate early user input into U.S. DOT research efforts to inform multi-phased research projects in real time, decrease the time between research and insight, and improve future activities</li>
        <li>Enable development of third-party ITS applications</li>
      </ul>
    </p>

    <h2 id="Scope">Scope</h2>
    <p>
      These guidelines apply to all research projects funded, either fully or partially, by the ITS JPO. Any ITS JPO-funded research project, including new awards, modifications and extensions to existing awards, and projects run by federal employees with ITS JPO funds is subject to these guidelines.
    </p>

    <h2 id="Audience">Audience</h2>
    <p>
      These guidelines are meant to be used by project teams when creating and publishing ITS JPO-funded research data, as well as by U.S. DOT Project Managers as standards for their projects producing ITS JPO-funded research data.
    </p>

    <h2 id="Guidelines">Guidelines</h2>
    <h3>A. Data Management Plan</h3>
    <p>
      Applicants must include a draft Data Management Plan (DMP) in their proposal. The ITS JPO recommends that this draft DMP does not exceed 10 pages. Project teams should review solicitation materials for any page limit requirements. The U.S. DOT will use the information in the draft DMP as part of its evaluation criteria for award and may ask for clarifications or changes from applicants prior to award to ensure the U.S. DOT and/or public receives appropriate data access. The draft DMP must provide a preliminary overview of data that may be collected or created through the project, and include the following:
      <ol>
        <li>
          DATA DESCRIPTION: Address the nature, scope, and scale of the data that will be collected or created. Define data elements within a detailed data dictionary.
        </li>
        <li>
          ACCESS POLICIES: Describe any access restrictions that may apply to your data.
        </li>
        <li>
          DATA STORAGE AND RETENTION: Describe where you intend to store your data and why you have chosen that particular option. Different systems may be designated for different types of data (e.g., public, non-public). Also describe how long you intend to retain your data in these systems.
        </li>
        <li>
          DATA RIGHTS: Describe who will hold the intellectual property rights for your data, and any other legal requirements that might need to be addressed.
        </li>
      </ol>
    </p>
    <p>
      The updated DMP must include updated version of all of the information in the draft DMP as well as the following:
      <ol>
        <li>
          TIMING: Describe when your data will be shared, and how frequently it will be updated.
        </li>
      </ol>
    </p>
    <p>
      It is recommended that DMPs do not initially exceed five pages more than the project’s preliminary DMP and should grow over the course of the project as more data are added and processes evolve. However, projects should refer to any solicitation or project documentation for specific page requirements. Awardees must then maintain the DMP throughout the period of performance with more detailed, updated information as it becomes available. At a minimum, the Awardee must update the DMP every six months and any time the Awardee shares a new dataset or changes data standards used. By the end of the project, the DMP must be a complete and accurate record of all data collected or created through the project. <br>
      The DMP must demonstrate the project's full compliance with these guidelines and all applicable U.S. DOT and federal policies and guidance.
    </p>

    <h3>B. Access</h3>
    <p>
      Consistent with federal policy <a href="https://www.congress.gov/bill/115th-congress/house-bill/4174/text#toc-H8E449FBAEFA34E45A6F1F20EFB13ED95" target="_blank" rel="noopener noreferrer">(OPEN Government Data Act<img class="in-line-external-link-icon" src="/images/icons/external-link_1b1b1b.svg" alt="External from DOT link icon" title="External link. Opens in a new tab."></a>),awardees must provide public access to data collected or created through the project unless a specific privacy, confidentiality, security, or other valid restriction is identified. This presumption in favor of openness must be reflected in the research project's DMP. Where valid restrictions to public access exist, awardees must provide controlled access to data that is necessary to achieve core research goals. For example, if the purpose of a project is to evaluate effectiveness of a new technology, the awardee must provide U.S. DOT evaluators access to enough data to support a scientifically-sound evaluation. Furthermore, awardees must make final research results and, where possible, sanitized versions of primary data available to the public.
    </p>
    <p>
      Final research results must satisfy the reporting and compliance guidelines as set forth in the <a href="https://www.transportation.gov/sites/dot.gov/files/docs/Official%20DOT%20Public%20Access%20Plan.pdf" target="_blank" rel="noopener noreferrer">U.S.DOT Public Access Plan<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab."></a>, including, but not limited to, the use of Open Researcher and Contributor ID (ORCID) numbers, the creation and maintenance of a research project record in the Transportation Research Board’s (TRB) Research in Progress (RiP) database, and the timely and complete submission of all required publications and associated digital datasets as such terms are defined in the <a href="https://www.transportation.gov/sites/dot.gov/files/docs/Official%20DOT%20Public%20Access%20Plan.pdf" target="_blank" rel="noopener noreferrer">U.S. DOT Public Access Plan<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab."></a>. Additional information about how to comply with the guidelines can be found at: <a href="https://ntl.bts.gov/public-access/how-comply" target="_blank" rel="noopener noreferrer">https://ntl.bts.gov/public-access/how-comply<img class="in-line-external-link-icon" src="/images/icons/external-link_1b1b1b.svg" alt="External from DOT link icon" title="External link. Opens in a new tab."></a>. The ITS JPO will assist awardees in compliance with these broader U.S. DOT guidelines.
    </p>

    <h3>C. Data Storage and Retention</h3>
    <p>
      Awardees must store their data in systems that provide an appropriate level of user access, functionality and data lifecycle management. For more guidelines on acceptable data storage systems for U.S. DOT, see <a href="https://ntl.bts.gov/public-access/guidelines-evaluating-repositories" target="_blank" rel="noopener noreferrer">Guidelines for Evaluating Repositories for Conformance to the DOT Public Access Plan<img class="in-line-external-link-icon" src="/images/icons/external-link_1b1b1b.svg" alt="External from DOT link icon" title="External link. Opens in a new tab."></a>. 
    </p>
    <p>
      Awardees may use different systems to store different types of data (e.g., public, non-public, primary, derived, streaming, static). The ITS JPO has designated several public and controlled access data systems that awardees can use to meet their data access and retention requirements, including registering metadata in the <a href="https://its.dot.gov/data/" target="_blank" rel="noopener noreferrer">ITS DataHub<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab."></a>. Applicants are encouraged to use these shared resources but can propose alternative systems for storing and providing access to data via their draft DMP and supplemental information. If ITS JPO determines that a proposed system does not provide an appropriate level of user access and functionality and data lifecycle management, the awardee must use the systems designated by the ITS JPO. Wherever possible, these determinations will be made prior to award.
    </p>
    <p>
      Datasets in ITS DataHub are required to be retained for a minimum of five years in accordance with the <a href="https://www.fhwa.dot.gov/legsregs/directives/orders/hoit-hq_.cfm" target="_blank" rel="noopener noreferrer">ITS DataHub Records Disposition Manual<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab."></a>, NARA schedule number <a href="https://www.archives.gov/files/records-mgmt/rcs/schedules/departments/department-of-transportation/rg-0406/daa-0406-2014-0003_sf115.pdf" target="_blank" rel="noopener noreferrer">DAA-0406-2014-0003<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab."></a>. Dataset types may include roadway traffic-related data such as vehicle latitude, longitude, heading and speed; travel times on road segments; traffic signal timing plans; rail transit schedules; and weather. This retention period begins when U.S. DOT first receives access to a dataset.
    </p>

    <h3>D. Data Rights</h3>
    <p>
      Consistent with federal policy, awardees must apply open licenses to data collected or created through the project so there are no restrictions on copying, publishing, distributing, transmitting, adapting, or otherwise using the information. The Open Source Initiative approves open source licenses that are "popular and widely used", a list of which can be found at <a href="https://opensource.org/licenses/category" target="_blank" rel="noopener noreferrer">https://opensource.org/licenses/category<img class="in-line-external-link-icon" src="/images/icons/external-link_1b1b1b.svg" alt="External from DOT link icon" title="External link. Opens in a new tab."></a>. The ITS JPO uses clauses in <a href="http://farsite.hill.af.mil/reghtml/regs/far2afmcfars/fardfars/far/52_227.htm#P254_51765" target="_blank" rel="noopener noreferrer">FAR 52.227-14<img class="in-line-external-link-icon" src="/images/icons/external-link_1b1b1b.svg" alt="External from DOT link icon" title="External link. Opens in a new tab."></a> to acquire unlimited rights to data for the government, though applicants can claim exemptions and propose limited rights data as described in <a href="http://farsite.hill.af.mil/reghtml/regs/far2afmcfars/fardfars/far/52_227.htm#P254_51765" target="_blank" rel="noopener noreferrer">FAR 52.227-14<img class="in-line-external-link-icon" src="/images/icons/external-link_1b1b1b.svg" alt="External from DOT link icon" title="External link. Opens in a new tab."></a>.Applicants must describe their proposed approach to data rights in their draft DMP, including, where applicable, a detailed rationale for limiting the government's rights to data.
    </p>
    <p>
      By law, works of the U.S. government are not copyrightable in the U.S. and are public domain. However, U.S. government works are copyrightable internationally, and so the ITS JPO intentionally waives this copyright abroad using Creative Commons Zero (CC0) 1.0.
    </p>
    <p>
      The public may use ITS JPO-funded research data as the basis for wholly proprietary and commercial systems. The ITS JPO requests that users of this research data disclose its lineage, but maintain no legal right to require disclosure. Notifications that ITS JPO work is used in a new system or project is greatly appreciated.
    </p>

    <h3>E. Data Standards</h3>
    <p>
      Consistent with federal policy, awardees must use standards to promote data interoperability and openness. Where existing standards are insufficient, awardees must describe in their DMP how they will contribute to the development of new standards.
    </p>
    <p>
      ITS JPO will issue a Digital Object Identifier (DOI) for ITS JPO-funded projects to make them more citable and shareable. The assigned DOI should be referenced on all research data documentation.
    </p>
    
    <h3>F. Timing</h3>
    <p>
      Awardees must provide data in near-real-time, via streaming or batch uploads, throughout the period of data collection or creation. The ITS JPO encourages awardees to provide sample or test datasets early in a project to refine data dissemination processes and formats, and use automated processes to cut down on the time from data generation to dissemination.
    </p>

    <h3>G. Metadata and Documentation</h3>
    <p>
      Awardees must provide metadata on all data described in their DMP. Awardees can provide additional metadata beyond the common core fields if desired, and reference more detailed documentation. Consistent with federal policy, ITS JPO will provide this metadata to the public via the <a href="https://its.dot.gov/data/" target="_blank" rel="noopener noreferrer">ITS DataHub<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab."></a> and other data catalogs to facilitate data discovery, unless a specific privacy, confidentiality, security, or other valid restriction is identified. Awardees can claim such restrictions when they submit their metadata.
    </p>
    <p>
      The ITS JPO may request more detailed documentation and data dictionaries where necessary for users to understand and use the data.
    </p>

    <h2 id="Implementation-Roles-and-Responsibilities">Implementation Roles and Responsibilities</h2>
    <p>
      There are several key roles for implementing these guidelines. The roles are as follows:
      <ul>
        <li>
          Awardee: The primary role is the project principal investigator and ITS DataHub data provider. Main responsibilities include: a) creating and maintaining a DMP for their respective research project; b) identifying, collecting, managing and retaining research data for their respective project as per their DMP; c) preparing and coordinating data submissions with ITS JPO; d) answering questions about their research data; e) responding to access requests for their controlled data; f) complying with all ITS JPO Data Access guidelines.
        </li>
        <li>
          U.S. DOT Project Manager: The primary role is oversight for the research project. A U.S. DOT Project Manager is assigned to each ITS JPO-funded project. Main responsibilities include: a) coordinating execution of the project with the ITS JPO-funded project team; b) receiving, reviewing and approving the project's DMP; c) coordinating with the ITS JPO on research project execution and data submission.
        </li>
        <li>
          ITS JPO: The primary role is to administer the ITS JPO Data Access guidelines and the <a href="" target="_blank" rel="noopener noreferrer">ITS DataHub<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab."></a>, as well as provide technical assistance to ITS JPO-funded project data providers, project awardees and project managers. Main responsibilities include: a) defining guidelines, data management standards and <a href="" target="_blank" rel="noopener noreferrer">ITS DataHub<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab."></a> guidelines; b) operating and maintaining the <a href="" target="_blank" rel="noopener noreferrer">ITS DataHub<img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab."></a>; c) providing technical assistance to awardees to create a DMP and successfully implement the project's DMP. While the ITS JPO expects awardees to fully implement their DMP, the ITS JPO will provide technical assistance to clarify any ambiguity around data management and assist the project in implementing their DMP.
        </li>
        <li>
          U.S. DOT CIO: The primary role is promoting the effective and efficient design and operation of all major information resource management processes within U.S. DOT.
        </li>
      </ul>
    </p>

    <h2 id="Definitions">Definitions</h2>
    <ul>
      <li>
        <strong>Applicants</strong>: Persons responding to a solicitation by the U.S. DOT for an ITS JPO-funded grant, contract, or other funding agreement.
      </li>
      <li>
        <strong>Award</strong>: Any funding agreement, such as a contract or grant, signed by a contracting officer that permits the expenditure of government funds or use of such government resources to a provider of the goods or services requested in the funding agreement.
      </li>
      <li>
        <strong>Awardee</strong>: An organizational entity or individual that receives a written agreement from a U.S. DOT contracting officer to receive funds from the ITS JPO and/or the U.S. DOT in order to carry out a project or activity.
      </li>
      <li>
        <strong>Confidentiality</strong>: The ability of only authorized users to access controlled access data.
      </li>
      <li>
        <strong>Confidential business information</strong>: Trade secrets and commercial or financial information obtained from a person and privileged or confidential.
      </li>
      <li>
        <strong>Controlled access</strong>: Restricting access to certain groups of persons due to data containing personally identifiable information (PII), information that threatens privacy of an individual or group, information that threatens confidentiality of a person or group, and/or contains confidential business information.
      </li>
      <li>
        <strong>Data</strong>: Recorded information, regardless of form or the media on which the data is recorded.
      </li>
      <li>
        <strong>Data discovery</strong>: The ability to find data from search engines, such as the ITS DataHub.
      </li>
      <li>
        <strong>Data integrity</strong>: The assurance that digital information is uncorrupted and can only be accessed or modified by those authorized to do so. Integrity involves maintaining the consistency, accuracy and trustworthiness of data over its entire life cycle.
      </li>
      <li>
        <strong>Data owner</strong>: The person or organization that has the authority, ability, and responsibility to access, create, modify, store, use, share, and protect data. Data owners have the right to delegate these privileges and responsibilities to other parties. Please note, for U.S. DOT-funded projects this may be U.S. DOT.
      </li>
      <li>
        <strong>Data provider</strong>: An individual or team that collects, prepares and/or submits research datasets to the ITS JPO.
      </li>
      <li>
        <strong>Data security</strong>: Protective digital privacy measures that are applied to prevent unauthorized access to computers, databases and websites. Data security also protects data from corruption.
      </li>
      <li>
        <strong>Data steward</strong>: At the direction of the data owner, the person or organization that is delegated the privileges and responsibilities to manage, control, and maintain the quality of a data asset throughout the data life cycle. The data steward may also apply appropriate protections, restrictions, and other safeguards depending on the nature of the data, subject to the direction of the data owner.
      </li>
      <li>
        <strong>Derived data</strong>: Data derived thorough analysis of other data and shared as part of a final report to communicate the methodology and findings of a research project.
      </li>
      <li>
        <strong>Limited rights data</strong>: Data, other than computer software, that embody trade secrets or are commercial or financial and confidential or privileged, to the extent that such data pertain to items, components, or processes developed at private expense, including minor modifications.
      </li>
      <li>
        <strong>Metadata</strong>: Structural or descriptive information about data such as content, format, source, rights, accuracy, provenance, frequency, periodicity, granularity, publisher or responsible party, contact information, method of collection, and other descriptions.
      </li>
      <li>
        <strong>Personally Identifiable Information (PII)</strong>: Information that alone or in conjunction with additional information can threaten the privacy or identity of an individual.
      </li>
      <li>
        <strong>Primary data</strong>: Data collected during research that has not had significant analysis performed on it.
      </li>
      <li>
        <strong>Privacy</strong>: The ability of an individual to keep his or her personal information to themselves.
      </li>
      <li>
        <strong>Public</strong>: The general public, as well as qualified researchers or other similar entities.
      </li>
      <li>
        <strong>Public access</strong>: Data asset is or could be made publicly available to all without restrictions.
      </li>
      <li>
        <strong>Research</strong>: Creative work undertaken on a systemic basis in order to increase the stock of knowledge, including knowledge of man, culture and society.
      </li>
      <li>
        <strong>Research project</strong>: A project designed to accomplish a research objective.
      </li>
    </ul>

