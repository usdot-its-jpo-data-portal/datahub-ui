    <h1>Guidelines</h1>

    <h2 id="Background-and-Purpose">Background and Purpose</h2>
    <p>
      The mission of the ITS JPO is to support "the overall advancement of ITS through investments in major
      research initiatives, exploratory studies and a deployment support program including technology transfer
      and training." To further this mission, the ITS JPO Data Center of Excellence is committed to providing
      timely access to public research data to support third-party research, evaluation, and application development
      in order to maximize the ITS JPO’s investment in ITS research initiatives. 
    </p>
    <p>
      These requirements outline ITS JPO's standards for maximizing access to ITS research data while protecting
      data integrity, data security, data management best practices, copyright laws, privacy, confidentiality,
      and confidential business information.
    </p>
    <p>
      These requirements build upon broader federal and United States Department of Transportation (U.S. DOT)
      data policies, including but not limited to:
    </p>
    <ul>
      <li>
        Open, Public, Electronic, and Necessary (OPEN) Government Data Act: Title II of
        <a href="https://www.congress.gov/bill/115th-congress/house-bill/4174/text#toc-H8E449FBAEFA34E45A6F1F20EFB13ED95" target="_blank" rel="noopener noreferrer">
          Foundations for Evidence-Based Policymaking Act (H.R. 4174) <img class="in-line-external-link-icon" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab.">
        </a>
      </li>
      <li>
        U.S. DOT Public Access Plan: 
        <a href="https://www.transportation.gov/sites/dot.gov/files/docs/Official DOT Public Access Plan.pdf" target="_blank" rel="noopener noreferrer">
          Increasing Access to Federally Funded Research Results<img class="in-line-dot-link-new-tab" src="/images/icons/external-tabs.svg" alt="New tab icon." title="Opens in a new tab.">
        </a>
      </li>
      <li>
        Regulatory requirements stipulated under Title 23 and Title 49 of the United States Code (USC)
      </li>
      <li>
        Office of Science and Technology Policy (OSTP) Memorandum:
        <a href="https://obamawhitehouse.archives.gov/sites/default/files/microsites/ostp/ostp_public_access_memo_2013.pdf" target="_blank" rel="noopener noreferrer">
