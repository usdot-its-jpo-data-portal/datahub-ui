    </p> 
    
    <h3>3.3	Re-Use, Redistribution, and Derivative Products Policies</h3>
    <p>
      The U.S. DOT is interested in who holds the intellectual property right(s) for the research project data, whether these rights will transfer to a data archive, whether any copyrights apply to the data, and other aspects that apply to the rights to use the data. Please provide details on these elements in this section.
    </p>
    <p>
      In accordance with the <a href="https://www.congress.gov/bill/115th-congress/house-bill/1770/text">OPEN Government Data Act</a>, to the extent practicable, data must be made available through an open license that is available at no cost to the public and with no restrictions on copying, publishing, distributing, transmitting, citing or adapting such asset. In this section, please provide the applicable licenses for each dataset and specify if an open license will be used. If the project is U.S. DOT-funded, an open license should be used for the dataset, unless otherwise restricted by controlled access. If a non-open license is used for this data, include a justification for why a non-open license is used.
    </p>
    <p>
      Suggested elements for this section include, but are not limited to:
      <ul>
        <li>
          <strong>Dataset Title:</strong> Please provide the title of the dataset(s) that use this license.
        </li>
        <li>
          <strong>License Used:</strong> List the license that is used for this/these dataset(s) (e.g. Creative Commons – BY version X.0) and provide a URL (or several) with more information on the license. Specify whether the license is an open license or a non-open license. An “’open license’ means a legal guarantee that a data asset is made available (A) at no cost to the public and (B) with no restrictions on copying publishing, distributing, transmitting, citing or adapting such asset. ” A non-open license does not adhere to all of the open license criteria.  
        </li>
        <li>
          <strong>Reason(s) for a Non-Open License:</strong> If a non-open license is used for a dataset, provide the reason for using a non-open license.
        </li>
      </ul>
    </p>

    <h3>3.4	Data Storage, Archiving and Preservation</h3>
    <p>
      Storing and retaining the data is a key part of the data steward’s responsibilities to manage, control, and maintain the quality of a data asset throughout the data lifecycle.  
    </p>
    <p>
      In this section, list all of the data storage systems that are used to store the research project’s data, provide details on those data systems, and specify how long the data will be stored in each system.
    </p>
    <p>
      A public access system provides full data access to the public. A controlled-access system restricts access to certain groups of persons due to data containing personally identifiable information (PII), information that threatens privacy of an individual or group, information that threatens confidentiality of a person or group, and/or contains confidential business information (CBI).  An applicant system is managed by the applicant, a U.S. DOT system is managed by the U.S. DOT, and a third-party system is a system that is managed by a person, group or organization other than the U.S. DOT or the applicant.
    </p>
    <p>
      Suggested elements for this section include, but are not limited to:
    </p>
    <h4>3.4.1	Storage Systems</h4>
    <p>
      <ul>
        <li>
