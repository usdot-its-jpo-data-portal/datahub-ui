      </li>
      <li>
        <strong>Data File Format(s):</strong> Provide the data file format(s) the data and/or datasets are anticipated to be in when they are made accessible to the U.S. DOT (e.g. .csv, .txt, json, newline json, etc.). If not yet known, explain to the best of your ability.
      </li>
      <li>
        <strong>Metadata:</strong> Provide the data file format(s) the datasets are anticipated to be in when they are made accessible to the U.S. DOT (e.g. .csv, .txt, json, newline json, etc.). If not yet known, explain to the best of your ability.
      </li>
    </ul>
      
    <h2 id="post-data-stewardship">3. Data Stewardship</h2>
    <p>Please provide details around data stewardship. Data stewardship involves proper management of data throughout the data lifecycle. This includes, but is not limited to, maintaining data quality and safeguarding the data.</p>
    
    <h3>3.1 Data Owner and Steward</h3>
    <p>
      Please define the data owner and data steward for this project’s datasets. It is acceptable for multiple datasets to have the same data owner and/or data steward. 
    </p>
    <p> 
      Suggested elements for this section include, but are not limited to:
      <ul>
        <li>
          <strong>Dataset Title:</strong> Please provide the data and/or datasets that are assigned the designated data owner and/or data steward. You may break out data and/or datasets that have separate data owners and/or data stewards.
        </li>
        <li>
          <strong>Data Owner:</strong> List who the data owner of the dataset is. The data owner is the person or organization that has the authority, ability, and responsibility to access, create, modify, store, use, share, and protect the data. Data owners have the right to delegate these privileges and responsibilities to other parties. Please note the data owner may be the U.S. DOT for U.S. DOT-funded research projects.
        </li>
        <li>
          <strong>Data Steward:</strong> List who the data steward for the dataset is. The data steward is, at the direction of the data owner, the person or organization that is delegated the privileges and responsibilities to manage, control, and maintain the quality of a data asset throughout the data lifecycle. The data steward may also apply appropriate protections, restrictions, and other safeguards depending on the nature of the data, subject to the direction of the data owner. The data steward may be the recipient of U.S. DOT-funded research projects or one of the recipient’s partner organizations.
        </li>
        <li>
          <strong>Federal Sponsor:</strong> Please list the U.S. DOT sponsor or project manager for this/these dataset(s).
        </li>
      </ul>
    </p>
    <h3>3.2 Access Level</h3>
    <p>
      Providing appropriate data access to safeguard data is a key aspect of data stewardship. In accordance with the OPEN Government Data Act, datasets must be made publicly accessible unless specific concerns require the data to have controlled-access. Please detail which data require controlled-access, reasons for controlling access to data, and methods taken to safeguard these data.
    </p>
    <p>
      Controlled-access is defined as restricting access to certain groups of persons due to data containing personally identifiable information (PII), information that threatens privacy of an individual or group, information that threatens confidentiality of a person or group, and/or contains confidential business information (CBI).  
    </p>
    
    <h4>3.2.1 Can all of the data from this project be shared with the public, or is controlled-access required for at least some of the data?</h4>
    <p>
      Please check the box for the access level that best applies to the project’s data:
      <ul>
        <li>
          <strong>Public Access:</strong> If there is no data requiring controlled-access, select the ‘Public Access’ checkbox.
        </li>
        <li>
          <strong>Controlled-Access:</strong> If there is data in one or more datasets requiring controlled-access (e.g. Personally Identifiable Information [PII] or Confidential Business Information [CBI]) select the ‘Controlled-Access’ checkbox.
        </li>
      </ul>
    </p>
    
    <h4>3.2.2	Datasets Requiring Controlled-Access</h4>
    <p>
      This section is only required if “Controlled-Access” is selected for Section 4.2.1.
    </p>
    <p>
      In this section, list all datasets that require controlled-access and why the datasets require controlled-access. Please provide all elements for each anticipated dataset that requires controlled-access.  
      <ul>
        <li>
          <strong>Dataset Title:</strong> Provide the the dataset title.
        </li>
        <li>
          <strong>Reasons for Controlled-access:</strong> For any data requiring controlled-access, please detail why the data requires controlled-access.
        </li>
        <li>
          <strong>Safeguarding Methods and Processes:</strong> Describe the methods and processes that are taken to safeguard the data requiring controlled-access, such as redacting data before providing public access to the dataset. Please elaborate on the specific methods, for example blurring license plates in videos, geofencing sensor data, removing persistent IDs, etc. Any privacy, ethical or confidentiality concerns raised due to data sharing should be described.
        </li>
      </ul>
    </p>

    <h4>3.2.3	Informed Consent</h4>
    <p>
      If your project contains human subject research, please describe how informed consent forms will permit sharing with the research community and whether additional steps, such as an Institutional Review Board (IRB) may be used to protect privacy and confidentiality.
    </p>

