      </li>
      <li>
        <strong>Dataset Title:</strong> Provide the dataset title.
      </li>
      <li>
        <strong>Description:</strong> A) Describe the purpose of the dataset including how it relates to the broader purpose of the research project. B) If using existing data, describe the relationship between the data you are collecting and existing data. C) Discuss the potential value the dataset will have over the long-term to the research project and other consumers.
      </li>
      <li>
